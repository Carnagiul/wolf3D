#include "libft.h"

long long				ft_pow_l(long long nbr)
{
	long long			pow;

	pow = 0;
	while (nbr != 0)
	{
		nbr /= 10;
		pow++;
	}
	return ((pow == 0) ? 1 : pow);
}
